// Remove Duplicates 
function removeDuplicates(str) {
    let arr = str.split(' ')
    let buffer = []
    arr.forEach(word => {
        if (buffer.indexOf(word) === -1) {
            buffer.push(word); 
        }
    })
    return buffer.join(' ');
}
console.log(removeDuplicates('lol lol meow meow unicorn'))

