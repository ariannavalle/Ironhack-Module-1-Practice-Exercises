//Palindrome check 
function isPalindrome(str) {
    let reversedString = str.toLowerCase().split(" ").join("");
    return reversedString.toLowerCase().split(" ").join("") === str.toLowerCase().split(" ").join("")? true : false;
}
console.log(isPalindrome('Race car'))


